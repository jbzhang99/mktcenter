package com.bizvane.couponservice.common.datavo;

public class VouTaskAnalysis {
	
	 private String	achievements	;//	用券订单业绩
	 private String	pieceNumber	;//	用券订单件数
	 private String	penNumber	;//	用券订单笔数
	 private String	discount	;//	券优惠金额
	 private String	orderNum	;//	券成交订单量
	 
	public String getAchievements() {
		return achievements;
	}
	public void setAchievements(String achievements) {
		this.achievements = achievements;
	}
	public String getPieceNumber() {
		return pieceNumber;
	}
	public void setPieceNumber(String pieceNumber) {
		this.pieceNumber = pieceNumber;
	}
	public String getPenNumber() {
		return penNumber;
	}
	public void setPenNumber(String penNumber) {
		this.penNumber = penNumber;
	}
	public String getDiscount() {
		return discount;
	}
	public void setDiscount(String discount) {
		this.discount = discount;
	}
	public String getOrderNum() {
		return orderNum;
	}
	public void setOrderNum(String orderNum) {
		this.orderNum = orderNum;
	}

	 
	 

}
